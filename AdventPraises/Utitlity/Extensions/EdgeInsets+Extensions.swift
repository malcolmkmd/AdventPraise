import SwiftUI

extension EdgeInsets {
    init(_ top: CGFloat = 0,
         leading: CGFloat = 0,
         bottom: CGFloat = 0,
         trailing: CGFloat = 0) {
        self.init(
            top: top,
            leading: leading,
            bottom: bottom,
            trailing: trailing)
    }
}
